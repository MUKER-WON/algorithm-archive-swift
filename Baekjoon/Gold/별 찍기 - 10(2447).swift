import Foundation

/// 프랙탈 도형: 재귀적이거나 반복적인 작업으로 만들어지는 패턴 도형
///
/// 키워드: block의 '가운데' 부분은 공백이다.
/// N = 3 일 때 y,x 좌표값으로 (0,0),(0,1),(0,2),(1,0),(공백),(1,2)...
///
/// ***
/// * * <- 5번째 순서에 공백이 생긴다.
/// ***
///
/// N = 9 일 때는, N = 3 일 때의 모양을 '*'이라 생각하고 가운데 공백을 두면 되는 도형을 만든다.
///
/// *********
/// * ** ** *
/// *********
/// ***   ***
/// * *   * * <- 마찬가지로 5번째 순서에 공백이 생긴다.
/// ***   ***
/// *********
/// * ** ** *
/// *********
///
func gold_별찍기_10() {
	let N = Int(readLine()!)!
	var arr = Array(repeating: Array(repeating: " ", count: N), count: N)
	
	func task(y: Int, x: Int, size: Int, blank: Bool) {
		// 가운데(5번째) 빈공간일 때
		guard !blank else { return }
		// size가 1이 됐을 때 해당 위치에 "*" 기입
		if size == 1 {
			arr[y][x] = "*"
			return
		}
		let new = size/3
		var count = 0
		// y부터 y+size까지 new만큼 씩 반복
		// size는 new의 3배니까 이 중첩 반복문은 9번 돈다.
		for i in stride(from: y, to: y+size, by: new) {
			for j in stride(from: x, to: x+size, by: new) {
				count += 1
				if count == 5 {
					task(y: i, x: j, size: new, blank: true)
				} else {
					task(y: i, x: j, size: new, blank: false)
				}
			}
		}
	}
	task(y: 0, x: 0, size: N, blank: false)
	arr.forEach { print($0.joined()) }
}

func gold_별찍기10_try2() {
	let N = Int(readLine()!)!
	var arr = Array(repeating: Array(repeating: " ", count: N), count: N)
	func task(y: Int, x: Int, size: Int) {
		if size == 1 {
			arr[y][x] = "*"
			return
		}
		let new = size / 3
		var checkFifth = 0
		for i in stride(from: y, to: y+size, by: new) {
			for j in stride(from: x, to: x+size, by: new) {
				checkFifth += 1
				if checkFifth != 5 {
					task(y: i, x: j, size: new)
				}
			}
		}
	}
	task(y: 0, x: 0, size: N)
	arr.forEach { print($0.joined()) }
}

